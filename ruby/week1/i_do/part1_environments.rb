########## The Console - a place to run some Ruby ##########

  $ irb

  > 2+2
   => 4
  > "hello world".upcase
   => "HELLO WORLD"
  > greeting = "say hi"
   => "say hi"
  > greeting.reverse
   => "ih yas"
 > 1 == 2
   => false
 > 2 == 2
   => true
 > 2 == "2"
   => false


