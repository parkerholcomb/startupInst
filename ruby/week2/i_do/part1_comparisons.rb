########## Comparison Operators ##########

  ruby comparison operators compares the two sides and returns a boolean ( true or false )

    ==
    !=
    >
    <
    >=
    <=

  remember we have different data types ...

    > "two" != 2
      => true

    similarly

    > "2" != 2
      => true



########## The Console - a place to run some Ruby ##########

  @youDo - open console and try some of these out

  $ irb

  examples

  > 2 == 2

  > "a" != "b"

  > "a" > "b"

  > 5 + 5 <= 10

  > "10" == 10

  > "10".to_i == 10

  > 'a' == 'a'
  > a = 'a'
